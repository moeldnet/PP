# Из предложенного текстового файла (text18-29.txt) вывести на экран его содержимое,
# количество символов в тексте. Сформировать новый файл, в который поместить текст в
# стихотворной форме предварительно поставив последнюю строку между второй и третьей.

# Чтение содержимого файла
with open('text18-29.txt', 'r', encoding='utf-8') as file:
    text = file.read()

# Вывод содержимого на экран
print(text)

# Подсчет количества символов
num_characters = len(text)
print(f'Количество символов в тексте: {num_characters}')

# Формирование нового текста с измененной строкой
lines = text.splitlines()
if len(lines) >= 3:
    # Перемещение последней строки между второй и третьей
    last_line = lines.pop()  # Удаляем последнюю строку
    lines.insert(2, last_line)  # Вставляем ее между второй и третьей

# Запись нового текста в файл
with open('new_text.txt', 'w', encoding='utf-8') as new_file:
    new_file.write('\n'.join(lines))

print('Новый файл с измененным текстом создан: new_text.txt')
